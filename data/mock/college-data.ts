function scaledCount(base: number, factor: number) {
  return Math.max(0, Math.round(base * factor));
}

function scaledDistance(base: number, factor: number) {
  return Number((base * factor).toFixed(1));
}

function scaledMinutes(base: number, factor: number) {
  return Math.max(2, Math.round(base * factor));
}

